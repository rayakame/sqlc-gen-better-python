// newRenderRequest builds a GenerateRequest like sqlc would send it. A nil
// catalog means an empty default schema.
func newRenderRequest(engine, options string, catalog *plugin.Catalog, queries []*plugin.Query) *plugin.GenerateRequest {
	if catalog == nil {
		catalog = &plugin.Catalog{DefaultSchema: "public", Schemas: []*plugin.Schema{{Name: "public"}}}
	}

	return &plugin.GenerateRequest{
		Settings:      &plugin.Settings{Engine: engine},
		Catalog:       catalog,
		Queries:       queries,
		PluginOptions: []byte(options),
	}
}

// runRenderPipeline constructs the Renderer exactly like internal.Handler:
// config -> type conversion -> driver -> transform -> render.
func runRenderPipeline(req *plugin.GenerateRequest) ([]*plugin.File, error) {
	cfg, err := config.NewConfig(req)
	if err != nil {
		return nil, err
	}
	convFunc, err := types.GetTypeConversionFunc(req.Settings.Engine)
	if err != nil {
		return nil, err
	}
	drv, err := driver.New(cfg)
	if err != nil {
		return nil, err
	}
	tf := transform.NewTransformer(cfg, req, convFunc)
	enums := tf.BuildEnums()
	tables := tf.BuildTables()
	queries := tf.BuildQueries(tables)
	if cfg.OmitUnusedModels {
		enums, tables = transform.FilterUnusedModels(enums, tables, queries)
	}

	return render.New(cfg, drv).RenderAll(enums, tables, queries)
}

func mustRenderFiles(t *testing.T, req *plugin.GenerateRequest) []*plugin.File {
	t.Helper()
	files, err := runRenderPipeline(req)
	if err != nil {
		t.Fatalf("render pipeline failed: %v", err)
	}

	return files
}

func renderedFileNames(files []*plugin.File) []string {
	names := make([]string, 0, len(files))
	for _, file := range files {
		names = append(names, file.Name)
	}
	slices.Sort(names)

	return names
}

func renderedFile(t *testing.T, files []*plugin.File, name string) string {
	t.Helper()
	for _, file := range files {
		if file.Name == name {
			return string(file.Contents)
		}
	}
	t.Fatalf("file %q not in output, got %v", name, renderedFileNames(files))

	return ""
}

// sqlcFileHeader rebuilds the header WriteSqlcHeader emits; the sqlc version
// comes from the same environment variable the writer reads.
func sqlcFileHeader(sourceFile string) string {
	header := "# Code generated by sqlc. DO NOT EDIT.\n# versions:\n#   sqlc " + os.Getenv("SQLC_VERSION") +
		"\n#   sqlc-gen-better-python " + config.PluginVersion + "\n"
	if sourceFile != "" {
		header += "# source file: " + sourceFile + "\n"
	}

	return header
}

// wantInOrder asserts that every part occurs in got, in the given order.
func wantInOrder(t *testing.T, got string, parts ...string) {
	t.Helper()
	rest := got
	for _, part := range parts {
		idx := strings.Index(rest, part)
		if idx < 0 {
			t.Fatalf("missing %q (in order) in output:\n%s", part, got)
		}
		rest = rest[idx+len(part):]
	}
}

func pgColumn(name, sqlType string, notNull bool) *plugin.Column {
	return &plugin.Column{Name: name, Type: &plugin.Identifier{Name: sqlType}, NotNull: notNull}
}

func pgParam(column *plugin.Column) *plugin.Parameter {
	return &plugin.Parameter{Column: column}
}

// pgItemsCatalog is a default-schema catalog with a single test_items table.
func pgItemsCatalog() *plugin.Catalog {
	return &plugin.Catalog{
		DefaultSchema: "public",
		Schemas: []*plugin.Schema{{
			Name: "public",
			Tables: []*plugin.Table{{
				Rel: &plugin.Identifier{Name: "test_items"},
				Columns: []*plugin.Column{
					pgColumn("id", "int4", true),
					pgColumn("name", "text", false),
				},
			}},
		}},
	}
}

func TestRenderAllFileSets(t *testing.T) {
	t.Parallel()

	t.Run("empty request emits only models file", func(t *testing.T) {
		t.Parallel()
		req := newRenderRequest("postgresql", `{"package":"testpkg","sql_driver":"asyncpg","emit_init_file":false}`, nil, nil)

		got := renderedFileNames(mustRenderFiles(t, req))
		want := []string{"models.py"}
		if !slices.Equal(got, want) {
			t.Errorf("file names = %v, want %v", got, want)
		}
	})

	t.Run("full request emits enums, models, one module per file and init", func(t *testing.T) {
		t.Parallel()
		catalog := pgItemsCatalog()
		catalog.Schemas[0].Enums = []*plugin.Enum{{Name: "mood", Vals: []string{"ok"}}}
		req := newRenderRequest(
			"postgresql",
			// debug: true must NOT emit a log file here; that happens in the handler.
			`{"package":"testpkg","sql_driver":"asyncpg","emit_init_file":true,"debug":true}`,
			catalog,
			[]*plugin.Query{
				{Name: "InsertA", Cmd: metadata.CmdExec, Text: "INSERT INTO test_items (id) VALUES (1)", Filename: "a.sql"},
				{Name: "DeleteA", Cmd: metadata.CmdExec, Text: "DELETE FROM test_items", Filename: "a.sql"},
				{Name: "DeleteB", Cmd: metadata.CmdExec, Text: "DELETE FROM test_items", Filename: "b.sql"},
			},
		)

		files := mustRenderFiles(t, req)
		got := renderedFileNames(files)
		want := []string{"__init__.py", "a.py", "b.py", "enums.py", "models.py"}
		if !slices.Equal(got, want) {
			t.Errorf("file names = %v, want %v", got, want)
		}

		// Both a.sql queries land in a.py, in source order.
		aFile := renderedFile(t, files, "a.py")
		bFile := renderedFile(t, files, "b.py")
		wantInOrder(t, aFile,
			`INSERT_A: typing.Final[str] = """-- name: InsertA :exec`,
			`DELETE_A: typing.Final[str] = """-- name: DeleteA :exec`,
			"async def insert_a(conn: ConnectionLike) -> None:",
			"async def delete_a(conn: ConnectionLike) -> None:",
		)
		wantInOrder(t, bFile,
			`DELETE_B: typing.Final[str] = """-- name: DeleteB :exec`,
		)
		// Queries stay isolated per source file: no cross-module leakage.
		if strings.Contains(aFile, "DELETE_B") {
			t.Errorf("a.py contains b.sql's DELETE_B constant:\n%s", aFile)
		}
		for _, leaked := range []string{"INSERT_A", "DELETE_A"} {
			if strings.Contains(bFile, leaked) {
				t.Errorf("b.py contains a.sql's %s constant:\n%s", leaked, bFile)
			}
		}
	})
}

func TestRenderAllUnsupportedCommand(t *testing.T) {
	t.Parallel()
	req := newRenderRequest(
		"postgresql",
		`{"package":"testpkg","sql_driver":"asyncpg","emit_init_file":false}`,
		nil,
		[]*plugin.Query{{
			Name:     "InsertItem",
			Cmd:      metadata.CmdExecLastId,
			Text:     "INSERT INTO test_items (id) VALUES (1)",
			Filename: "queries.sql",
		}},
	)

	files, err := runRenderPipeline(req)
	if files != nil {
		t.Errorf("files = %v, want nil on error", renderedFileNames(files))
	}
	want := `unsupported cmd ":execlastid" for driver "asyncpg"`
	if err == nil || err.Error() != want {
		t.Errorf("err = %v, want %q", err, want)
	}
}

func TestRenderInitFile(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name    string
		options string
		want    string
	}{
		{
			name:    "docstrings none",
			options: `{"package":"testpkg","sql_driver":"asyncpg","emit_init_file":true}`,
			want:    sqlcFileHeader(""),
		},
		{
			name:    "docstrings google",
			options: `{"package":"testpkg","sql_driver":"asyncpg","emit_init_file":true,"docstrings":"google"}`,
			want: sqlcFileHeader("") +
				`"""Package containing queries and models automatically generated using sqlc-gen-better-python."""` + "\n",
		},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			req := newRenderRequest("postgresql", tc.options, nil, nil)

			got := renderedFile(t, mustRenderFiles(t, req), "__init__.py")
			if got != tc.want {
				t.Errorf("__init__.py mismatch\ngot:\n%q\nwant:\n%q", got, tc.want)
			}
		})
	}
}
